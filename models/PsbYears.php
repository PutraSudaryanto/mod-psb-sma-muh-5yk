			$courses = PsbCourses::getCourse(1, 'data');
			if($courses != null) {
				foreach($courses as $key => $val) {
					$course=new PsbYearCourse;
					$course->year_id=$this->year_id;
					$course->course_id=$val->course_id;
					$course->save();
				}
			}
		}
